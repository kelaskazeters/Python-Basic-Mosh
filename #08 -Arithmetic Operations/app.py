# Arithmetic Operator
# Additions
print(10 + 3)

# Substraction
print(10 - 3)

# Multiplication
print(10 * 3)

# Division
print(10 / 3)

# Eksponen / Pangkat
print(10**3)

# Modulus
print(10 % 3)

# Floor Division
print(10 // 3)

# Assignment Operator
